  <h1 class="h1footer">présentation</h1>
  <p>Le laboratoire de Recherche pour le Développement des Systèmes Informatisés (LRDSI) de l’université Saad Dahlab –Blida1, a été agréé en mai 2002 pour accompagner le département d’informatique créé en 1999, dans ses activités pédagogiques et scientifiques. Il est composé de quatre équipes regroupant plus de 30 enseignants chercheurs et 32 doctorants (LMD et classique). </p>
  <p>Les missions du LRDSI s’articulent autour de trois axes principaux : recherche, valorisation - transfert de technologie et formation. Sa vocation est de développer un savoir-faire, au service de la société en liaison étroite avec les disciplines Ingénierie, Sciences Humaines et Sociales, Sciences de l'environnement et Sciences de la Vie. </p>
  <p>Les thèmes de recherche du laboratoire couvrent un large spectre des sciences de l’informatique à dominante logicielle et incluent à la fois, des aspects fondamentaux et des aspects appliqués : algorithmique, combinatoire, graphes, programmation, génie logiciel, vérification et preuves, parallélisme, calcul à haute performance, réseaux, bases de données, extraction et représentation des connaissances, apprentissage, fouille de données, interaction homme-machine, aide à la décision, etc. </p> 
  <p>Les enseignants-chercheurs du LRDSI participent activement à la formation des étudiants inscrits dans les spécialités informatiques des trois niveaux du L (Licence), M (Master) et D (Doctorat) mis en place à chaque rentrée universitaire. </p>
  <p>Nadjia Benblidia Directrice du Laboratoire</p>
